'''18. Вводити з клавіатури числа в рядок до тих пір,
поки не буде введено число 0. Видалити всі
непарні числа даного списку та вивести його.'''

list = []
i = int(input('Вводите числа(для остановки введите 0): '))
while i != 0:
    list.append(i)
    i = int(input('Вводите числа(для остановки введите 0): '))
j = 0
while j <= len(list)-1:
    if list[j] % 2 != 0:
        list.remove(list[j])
    else:
        j = j + 1
print(list)
